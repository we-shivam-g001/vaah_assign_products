    public static function syncProductsWithCustomers()
    {
        $all_users = Product::select('id')->get()->pluck('id')->toArray();
        $all_roles = self::select('id')->get();

        if (!$all_roles) {
            return false;
        }

        foreach ($all_roles as $role) {
            $role->products()->syncWithoutDetaching($all_users);
        }
        return true;

    }
